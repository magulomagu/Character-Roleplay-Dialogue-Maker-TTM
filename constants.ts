export const getAnalysisPrompt = (text: string): string => `
あなたはテキストからキャラクターの特徴を分析する専門家です。以下のテキストを読み込み、指定されたJSON形式でキャラクターの「パーソナリティ」、「言語スタイル」、「長期記憶（属性）」を抽出してください。

# 制約
- パーソナリティは、単なる形容詞の羅列ではなく、キャラクターの思考様式、価値観、行動原理がわかるように具体的に記述してください。個性が際立つように、深掘りした分析を心がけてください。
- 言語スタイルは、口調、文の長さ、頻繁に使う単語や言い回し（感嘆詞、一人称、語尾など）を具体的に分析してください。
- 長期記憶は、キャラクターに関する事実情報（名前、年齢、職業、人間関係、過去の出来事など）をキーとバリューのペアで抽出してください。テキストから推測できる範囲で構いません。

# 入力テキスト
${text}

# 出力形式 (JSON)
\`\`\`json
{
  "personality": "（ここに分析結果を記述）",
  "languageStyle": "（ここに分析結果を記述）",
  "longTermMemory": {
    "名前": "（キャラクターの名前）",
    "関係者": "（友人など）"
  }
}
\`\`\`
`;

